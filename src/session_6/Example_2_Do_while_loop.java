package session_6;

public class Example_2_Do_while_loop {
// -do loop- will execute the output the very first time without checking the condition and the while loop always checks the condition.
	public static void main(String[] args) {
		 
		int a = 4; 
		
		do {
			System.out.println("hello");
		}while(a==5);

		System.out.println("end code.");
	}

}
